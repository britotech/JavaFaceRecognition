    private FrmDashboard dashboard;

    public FrmMain() {
        initComponents();
        setBackground(new Color(0, 0, 0, 0));

        dashboard = new FrmDashboard();
        menu.initMoving(FrmMain.this);
        setForm(dashboard);

        menu.addEventMenuSelected(new EventMenuSelected() {
            @Override
            public void selected(int index) {

                switch (index) {
                    case 1:
                        exibirFormCaptura();
                        break;
                    case 2:
                        exibirFormReconhecimento();
                        break;
                    case 3:
                        finalizar();
                        break;
                    default:
                        setForm(dashboard);
                }
            }
        });

        setForm(dashboard);
    }

    private void exibirFormCaptura() {
        new FrmCaptura(this, true).setVisible(true);
        setForm(dashboard);
    }

    private void exibirFormReconhecimento() {
        new FrmReconhecimento(this, true).setVisible(true);
        setForm(dashboard);
    }

    private void finalizar() {
        this.dispose();
    }

    private void setForm(JComponent com) {
        mainPanel.removeAll();
        mainPanel.add(com);
        mainPanel.repaint();
        mainPanel.revalidate();
    }

